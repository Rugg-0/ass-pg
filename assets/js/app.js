function list(list, data, imgSrc){
    var list = document.getElementById(`${list}`);
    for(i = 0; i < data.length; i++){
        var row = `<div class="box">
                        <div class="img-box">
                            <img src="./assets/img/${imgSrc}/${data[i].trait}.png" />
                        </div>
                        <h3>${data[i].trait}</h3>
                        <h3>Weight ${data[i].weight}</h3>
                        <h3>Occurrence: ${data[i].occurrence}</h3>
                   </div>`
        list.innerHTML += row
    }
}


